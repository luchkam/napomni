function getLengthHint(sectionKey, mode) {
  if (sectionKey === 'what_can_do') {
    return 'Длина: 8-12 коротких строк/предложений.';
  }

  if (mode === 'show_more') {
    return 'Длина: 7-10 коротких строк/предложений.';
  }

  return 'Длина: 5-9 коротких строк/предложений.';
}

export function buildPrompt({ sectionKey, userText = '', mode = 'section' }) {
  const section = getSectionByKey(sectionKey) ?? getSectionByKey('what_can_do');

  return `Режим: demo.
Раздел: ${section.shortTitle} (${section.sectionKey}).
Инструкция раздела: ${section.promptFragment}
Сообщение пользователя: ${userText || '(пусто)'}
Сценарий ответа: ${mode}
${getLengthHint(section.sectionKey, mode)}

Ответ должен быть читаемым в Telegram, без markdown-маркеров и без таблиц.
Избегай частых повторов одних и тех же фраз о demo и одинаковых финальных CTA подряд.`;
}
