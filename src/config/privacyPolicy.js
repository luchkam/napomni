export function splitPrivacyPolicy(maxLength = 3600) {
  const paragraphs = PRIVACY_POLICY_TEXT.split('\n\n');
  const chunks = [];
  let currentChunk = '';

  for (const paragraph of paragraphs) {
    const candidate = currentChunk ? `${currentChunk}\n\n${paragraph}` : paragraph;

    if (candidate.length <= maxLength) {
      currentChunk = candidate;
      continue;
    }

    if (currentChunk) {
      chunks.push(currentChunk);
      currentChunk = paragraph;
      continue;
    }

    // Defensive fallback for unusually long single paragraph.
    let offset = 0;
    while (offset < paragraph.length) {
      chunks.push(paragraph.slice(offset, offset + maxLength));
      offset += maxLength;
    }
    currentChunk = '';
  }

  if (currentChunk) {
    chunks.push(currentChunk);
  }

  return chunks;
}
